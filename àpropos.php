<h2>À Propos Du Projet</h2>

<p>
    Ce site a été développé dans le cadre de l’épreuve E6 du <strong>BTS SIO SLAM</strong> (Services Informatiques Aux Organisations - Solutions Logicielles Et Applications Métiers).
</p>

<p>
    L’objectif de ce projet est de permettre la création et la consultation de <strong>Guidi</strong> pour aider les personnes en situation de handicap, dans différents domaines comme :
</p>

<ul>
    <li>💻 Informatique</li>
    <li>🔧 Mécanique</li>
    <li>🧰 Bricolage</li>
    <li>🖱️ Logiciels</li>
</ul>

<p>
    L’interface permet d’ajouter, modifier, supprimer et télécharger ces guides au format PDF.
</p>

<p>
    Le projet a été réalisé avec les technologies suivantes :
</p>

<ul>
    <li>🖥️ HTML, CSS, PHP</li>
    <li>🗃️ Base De Données MySQL</li>
    <li>📄 Génération De PDF Avec Dompdf</li>
    <li>💡 Hébergé Localement Via Un Environnement WAMP</li>
</ul>

<p>
    Ce projet répond à une problématique réelle : rendre l'information plus accessible à tous, en particulier aux personnes qui rencontrent des difficultés physiques ou cognitives.
</p>

